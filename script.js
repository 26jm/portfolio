const UPSTAGE_MODEL = 'solar-pro3';
const UPSTAGE_KEY_STORAGE = 'upstage_api_key';
const IS_LOCAL_FILE = location.protocol === 'file:';

/* =============================================
   챗봇 — API 키 처리 (로컬 file: 전용)
   ============================================= */
function getStoredApiKey() {
  return localStorage.getItem(UPSTAGE_KEY_STORAGE);
}

function saveApiKey(key) {
  localStorage.setItem(UPSTAGE_KEY_STORAGE, key.trim());
}

function clearStoredApiKey() {
  localStorage.removeItem(UPSTAGE_KEY_STORAGE);
}

function promptForApiKey() {
  const key = window.prompt('Upstage API 키를 붙여넣어 주세요 (up_으로 시작)');
  if (!key || !key.trim()) return null;
  saveApiKey(key);
  return key.trim();
}

function resolveApiKey() {
  const stored = getStoredApiKey();
  if (stored) return stored;
  return promptForApiKey();
}

/* =============================================
   챗봇 — Upstage API 호출
   ============================================= */
async function callChatApi(messages) {
  if (IS_LOCAL_FILE) {
    const apiKey = resolveApiKey();
    if (!apiKey) {
      throw new Error('API_KEY_MISSING');
    }

    const response = await fetch(UPSTAGE_API_URL, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        Authorization: `Bearer ${apiKey}`,
      },
      body: JSON.stringify({ model: UPSTAGE_MODEL, messages }),
    });

    const data = await response.json();

    if (response.status === 401) {
      clearStoredApiKey();
      throw new Error('API_KEY_INVALID');
    }

    if (!response.ok) {
      throw new Error('API_ERROR');
    }

    return data;
  }

  const response = await fetch('/api/chat', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ messages }),
  });

  const data = await response.json();

  if (!response.ok) {
    throw new Error('API_ERROR');
  }

  return data;
}

/* =============================================
   챗봇 — UI 초기화 & 이벤트
   ============================================= */
(function initChatbot() {
  const chatbot = document.getElementById('chatbot');
  const chatFab = document.getElementById('chatFab');
  const chatPanel = document.getElementById('chatPanel');
  const chatMessages = document.getElementById('chatMessages');
  const chatLoading = document.getElementById('chatLoading');
  const chatForm = document.getElementById('chatForm');
  const chatInput = document.getElementById('chatInput');
  const chatSend = document.getElementById('chatSend');

  let isOpen = false;
  let isSending = false;
  const conversationHistory = [];

  /* 패널 열기/닫기 */
  function toggleChat() {
    isOpen = !isOpen;
    chatbot.classList.toggle('chatbot--open', isOpen);
    chatFab.setAttribute('aria-expanded', String(isOpen));
    chatPanel.setAttribute('aria-hidden', String(!isOpen));

    if (isOpen) {
      chatInput.focus();
    }
  }

  chatFab.addEventListener('click', toggleChat);

  /* 메시지 DOM 추가 */
  function appendMessage(text, role) {
    const msg = document.createElement('div');
    msg.className = `chatbot__msg chatbot__msg--${role}`;
    const p = document.createElement('p');
    p.textContent = text;
    msg.appendChild(p);
    chatMessages.appendChild(msg);
    chatMessages.scrollTop = chatMessages.scrollHeight;
  }

  /* 로딩 표시 토글 */
  function setLoading(visible) {
    chatLoading.hidden = !visible;
    if (visible) {
      chatMessages.scrollTop = chatMessages.scrollHeight;
    }
  }

  /* 전송 중 UI 잠금 */
  function setSendingState(sending) {
    isSending = sending;
    chatInput.disabled = sending;
    chatSend.disabled = sending;
  }

  /* API 요청용 messages 배열 구성 */
  function buildMessages() {
    return [
      { role: 'system', content: SYSTEM_PROMPT },
      ...conversationHistory,
    ];
  }

  /* 메시지 전송 */
  async function handleSend(e) {
    e.preventDefault();

    const text = chatInput.value.trim();
    if (!text || isSending) return;

    chatInput.value = '';
    chatInput.style.height = 'auto';
    appendMessage(text, 'user');
    conversationHistory.push({ role: 'user', content: text });

    setSendingState(true);
    setLoading(true);

    try {
      const data = await callChatApi(buildMessages());
      const reply = data.choices?.[0]?.message?.content;

      if (!reply) {
        throw new Error('API_ERROR');
      }

      appendMessage(reply, 'bot');
      conversationHistory.push({ role: 'assistant', content: reply });
    } catch (err) {
      if (err.message === 'API_KEY_MISSING') {
        appendMessage('API 키가 필요해요. 다시 질문하시면 키 입력 창이 뜹니다.', 'error');
        conversationHistory.pop();
      } else if (err.message === 'API_KEY_INVALID') {
        appendMessage('키가 올바르지 않아요. 다시 질문하시면 새로 입력받을게요', 'error');
        conversationHistory.pop();
      } else {
        appendMessage('잠시 후 다시 시도해 주세요', 'error');
        conversationHistory.pop();
      }
    } finally {
      setLoading(false);
      setSendingState(false);
      chatInput.focus();
    }
  }

  chatForm.addEventListener('submit', handleSend);

  /* Enter로 전송, Shift+Enter로 줄바꿈 */
  chatInput.addEventListener('keydown', (e) => {
    if (e.key === 'Enter' && !e.shiftKey) {
      e.preventDefault();
      chatForm.requestSubmit();
    }
  });

  /* textarea 자동 높이 조절 */
  chatInput.addEventListener('input', () => {
    chatInput.style.height = 'auto';
    chatInput.style.height = `${Math.min(chatInput.scrollHeight, 100)}px`;
  });
})();
